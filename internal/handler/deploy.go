// Cloudflare Zone Deploy — สร้าง zone + DNS record อัตโนมัติ
// =============================================================================

// DeployCloudflareZone สร้าง Cloudflare zone สำหรับ domain ลูกค้า
//
// Flow:
//  1. สร้าง zone ใน CF (หรือดึง zone เดิมถ้ามีอยู่แล้ว)
//  2. เพิ่ม A record ชี้ไป server IP (proxied = ซ่อน IP ☁️)
//  3. เพิ่ม A record สำหรับ www subdomain
//  4. ตั้ง SSL mode = flexible (ไม่ต้อง cert บน server)
//  5. Return nameservers ให้ frontend แสดงบอกลูกค้า
//
// Parameters:
//   - cfClient: Cloudflare API client (ต้องไม่เป็น nil)
//   - domain: ชื่อ domain เช่น "huay99.com"
//   - serverIP: IP ของ server จริง (จะถูกซ่อนหลัง CF proxy)
func DeployCloudflareZone(cfClient *cloudflare.Client, domain, serverIP string) DeployResult {
	// ── 1. สร้าง Zone ──
	zone, err := cfClient.CreateZone(domain)
	if err != nil {
		log.Printf("❌ CF Deploy: สร้าง zone %s ไม่สำเร็จ: %v", domain, err)
		return DeployResult{
			Success: false,
			Message: fmt.Sprintf("สร้าง Cloudflare zone ไม่สำเร็จ: %v", err),
		}
	}

	// ── 2. เพิ่ม A record (root domain) — proxied ☁️ ──
	if err := cfClient.AddDNSRecord(zone.ZoneID, "A", domain, serverIP, true); err != nil {
		log.Printf("⚠️ CF Deploy: เพิ่ม A record %s ไม่สำเร็จ: %v", domain, err)
		// ไม่ return error — zone สร้างสำเร็จแล้ว แค่ DNS ยังไม่ได้เพิ่ม
	}

	// ── 3. เพิ่ม A record (www subdomain) — proxied ☁️ ──
	if err := cfClient.AddDNSRecord(zone.ZoneID, "A", "www."+domain, serverIP, true); err != nil {
		log.Printf("⚠️ CF Deploy: เพิ่ม www A record ไม่สำเร็จ: %v", err)
	}

	// ── 4. ตั้ง SSL mode = flexible ──
	// flexible = CF จัดการ HTTPS ให้, ไม่ต้อง cert บน origin server
	if err := cfClient.SetSSLMode(zone.ZoneID, "flexible"); err != nil {
		log.Printf("⚠️ CF Deploy: ตั้ง SSL mode ไม่สำเร็จ: %v", err)
	}

	log.Printf("✅ CF Deploy: %s สำเร็จ — NS: %v (zone: %s)", domain, zone.NameServers, zone.ZoneID)

	return DeployResult{
		Success:     true,
		Message:     fmt.Sprintf("สร้างเว็บ %s สำเร็จ — กรุณาเปลี่ยน Nameserver ของโดเมน", domain),
		Nameservers: zone.NameServers,
		CFZoneID:    zone.ZoneID,
	}
}
